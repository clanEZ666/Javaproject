package Cycles;

public class FizzBuzz {
    public static void main(String[] args) {

        int n = 33;

        for(int i = 1; i <= n; i++) {
            if(i % 3 == 0) {
                System.out.printf("Fizz");
            } else if (i % 5 == 0) {
                System.out.println("Brizz");
            }
        }





    }
}
